<template>
  <div>
    <h1>Car id {{ id }}</h1>
  </div>
</template>

<script>

export default {
  name: 'Car',
  data () {
    return {
      id: this.$router.currentRoute.params['id']
    }
  },
  watch: {
    $route (toR, fromR) {
      this.id = toR.params['id']
    }
  }
}
</script>

